def test_parse_npm_priority(tmp_path):
    """package-lock.json should be preferred over yarn.lock."""
    pkg_json = tmp_path / "package.json"
    pkg_json.write_text(PACKAGE_JSON)

    # Write both lockfiles
    (tmp_path / "package-lock.json").write_text(PACKAGE_LOCK_V2)
    (tmp_path / "yarn.lock").write_text(YARN_LOCK)

    result = parse_npm(tmp_path, include_dev=False)
    assert "package-lock.json" in result.source_file


def test_parse_npm_from_content():
    """Content-based entry point routes correctly."""
    bundle = {
        "package.json": PACKAGE_JSON,
        "package-lock.json": PACKAGE_LOCK_V2,
    }
    result = parse_from_content("npm", bundle, include_dev=False)
    assert result.ecosystem == "npm"
    assert len(result.deps) > 0
    assert "package-lock.json" in result.source_file


def test_parse_npm_from_content_yarn():
    """Content-based parser falls back to yarn.lock."""
    bundle = {
        "package.json": PACKAGE_JSON,
        "yarn.lock": YARN_LOCK,
    }
    result = parse_from_content("npm", bundle, include_dev=False)
    assert result.ecosystem == "npm"
    assert "yarn.lock" in result.source_file


def test_parse_npm_from_content_package_json_only():
    """Falls back to package.json when no lockfile present."""
    bundle = {"package.json": PACKAGE_JSON}
    result = parse_from_content("npm", bundle, include_dev=False)
    assert result.ecosystem == "npm"
    assert "package.json" in result.source_file
    assert len(result.deps) == 2  # express + node-fetch (no dev)
