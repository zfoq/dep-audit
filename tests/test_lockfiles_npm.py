"""Tests for npm lockfile parsers."""

from __future__ import annotations

import json
import textwrap

from dep_audit.lockfiles_pkg import parse_from_content, parse_npm
from dep_audit.lockfiles_pkg.npm import (
    _parse_package_json_content,
    _parse_package_lock_json_content,
    _parse_pnpm_lock_yaml_content,
    _parse_yarn_lock_content,
)

# ---------------------------------------------------------------------------
# package-lock.json v2/v3
# ---------------------------------------------------------------------------

PACKAGE_JSON = json.dumps({
    "name": "my-app",
    "dependencies": {
        "express": "^4.18.0",
        "node-fetch": "^3.0.0",
    },
    "devDependencies": {
        "jest": "^29.0.0",
    },
})

PACKAGE_LOCK_V2 = json.dumps({
    "name": "my-app",
    "lockfileVersion": 2,
    "packages": {
        "": {"name": "my-app"},
        "node_modules/express": {"version": "4.18.2"},
        "node_modules/node-fetch": {"version": "3.3.1"},
        "node_modules/jest": {"version": "29.7.0", "dev": True},
        "node_modules/body-parser": {"version": "1.20.2"},
        "node_modules/@types/node": {"version": "20.10.0", "dev": True},
    },
})


def test_parse_package_lock_v2():
    result = _parse_package_lock_json_content(PACKAGE_LOCK_V2, PACKAGE_JSON, False)
    assert result.ecosystem == "npm"
    names = {d.name for d in result.deps}
    assert "express" in names
    assert "node-fetch" in names
    assert "body-parser" in names
    # dev deps excluded
    assert "jest" not in names
    assert "@types/node" not in names


def test_parse_package_lock_v2_with_dev():
    result = _parse_package_lock_json_content(PACKAGE_LOCK_V2, PACKAGE_JSON, True)
    names = {d.name for d in result.deps}
    assert "jest" in names
    assert "@types/node" in names
    assert "express" in names


def test_parse_package_lock_v2_direct_detection():
    result = _parse_package_lock_json_content(PACKAGE_LOCK_V2, PACKAGE_JSON, False)
    dep_map = {d.name: d for d in result.deps}
    assert dep_map["express"].is_direct is True
    assert dep_map["node-fetch"].is_direct is True
    assert dep_map["body-parser"].is_direct is False


